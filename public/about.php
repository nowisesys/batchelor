<h3>About Batchelor</h3>
<p>
    This system is built on top of Batchelor, a tool for creating web applications 
    that needs to start jobs that will run for a long period of time, perhaps hours 
    or weeks.
</p>
<a class="w3-btn w3-blue" href="https://nowise.se/oss/batchelor/" style="min-width: 90px">Project</a>

<p>
    Batchelor provides job queue management, web services (SOAP/JSON) and responsive 
    web design for your application. Its job queue supports running jobs in parallell 
    and comes with command line tools for interacting with the scheduler.
</p>
<a class="w3-btn w3-deep-orange" href="api/docs" style="min-width: 90px">Services</a>

<p>
    Since version 2 it supports building distributed system with multiple remote
    queues. Using composer it can easily be integrated in your project and its
    API can be used as building bricks in your project.
</p>
